private fun calc(data: IntArray) {
    val fishes = data.toMutableList()
    val startTime = Date().time;
    for (day in 1..256) {
        val startDayTime = Date().time;
        val newFishes = mutableListOf<Int>()
        fishes.forEachIndexed { index, fish ->
            fishes[index]--
            if (fish == 0) {
                newFishes.add(8);
                fishes[index] = 6;
            }
        }
        fishes.addAll(newFishes);
        val size = fishes.size;
        val endDayTime = Date().time;
        val total = endDayTime - startTime
        val time = endDayTime - startDayTime
        println("Day: $day Fish: $size Total Time: $total Day Time: $time")
    }
    println(fishes.size);
}

