fun calcLine1(testData: Array<String>) {
    val errorStack = mutableListOf<Char>()
    val scoreList = mutableListOf<Long>()
    testData.forEach data@{ line ->
        val stack = mutableListOf<Char>()
        line.forEach { sign ->
            if (sign == '{' || sign == '(' || sign == '[' || sign == '<') {
                stack.add(sign)
            } else {
                if (!checkSign(sign, stack)) {
                    errorStack.add(sign)
                    return@data
                }
            }
        }
        if (stack.size > 0) {
            var score = 0L
            stack.asReversed().map { sign ->
                score *= 5
                when (sign) {
                    '(' -> score += 1
                    '[' -> score += 2
                    '{' -> score += 3
                    '<' -> score += 4
                }
            }
            scoreList.add(score)
        }
    }
    scoreList.sort()
    println(scoreList[scoreList.size / 2])

    val sum = errorStack.map { sign ->
        when (sign) {
            ')' -> 3
            ']' -> 57
            '}' -> 1197
            '>' -> 25137
            else -> 0
        }
    }.sum()
    println(sum)
}

fun checkSign(sign: Char, stack: MutableList<Char>): Boolean {
    val lastSign = stack.removeLast()
    when (sign) {
        ')' -> return lastSign == '('
        ']' -> return lastSign == '['
        '}' -> return lastSign == '{'
        '>' -> return lastSign == '<'
    }
    return false
}




